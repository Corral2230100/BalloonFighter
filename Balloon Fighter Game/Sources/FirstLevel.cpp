void block(int sizex, int sizey, int posx, int posy)
{
	Object* m_Block = Engine::Get().m_World->Create("Box");
	(*m_Block->Size()) = Vector2{ sizex,sizey };
	m_Block->SetPosition(static_cast<float>(posx), static_cast<float>(posy));
	CCollider* _collider = m_Block->AddComponent<CCollider>();
	BoxCollider* TempBox = new BoxCollider(_collider);
	TempBox->Corners =
	{
		Vector2{static_cast<int>(m_Block->X()),static_cast<int>(m_Block->Y()) },
		Vector2{static_cast<int>(m_Block->X() + m_Block->Size()->x),static_cast<int>(m_Block->Y() + m_Block->Size()->y)}
	};
	_collider->SetCollider(TempBox);
	TempBox = nullptr;
	m_Block = nullptr;
	_collider = nullptr;
}


